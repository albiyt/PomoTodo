package net.juancarlosfernandez.android.pomotodo;

public class Employee {
	
	private int id;
	private String type;
	private String name;
	private int age;
	
	public void setType(String value) {
		type = value;
	}

	public void setName(String value) {
		name = value;
	}

	public void setId(int value) {
		id = value;
	}

	public void setAge(int value) {
		age = value;
	}

}
